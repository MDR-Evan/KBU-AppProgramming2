package com.example.myapplication;

public class Employee {
    public String id;
    public String name;
    public String gender;
    public int salary;
    public String imageUrl;

    public Employee(String id, String name, String gender, int salary, String imageUrl) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.salary = salary;
        this.imageUrl = imageUrl;
    }
}
